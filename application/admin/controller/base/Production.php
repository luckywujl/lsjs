        ->where(['production_id'=>$production_id,'company_id'=>$this->auth->company_id])
        ->find();   
       if ($production_info){
       	    	   
       		$this->success(null,null,$production_info);
       	}else {
        	 $this->error('产品信息异常，请核实',null,null);
       	}   	 
       }   
    }



